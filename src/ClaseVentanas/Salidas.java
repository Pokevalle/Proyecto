public void procesarSalidaTemporal() {

    String placa = Sp.getText().trim();

    if (placa.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese una placa.");
        return;
    }

    SubTicket st = new SubTicket();
    SubParqueo sp = new SubParqueo();

    Integer id = st.obtenerTicketIdActivo(placa);

    if (id == null) {
        JOptionPane.showMessageDialog(this, "No existe ticket activo.");
        return;
    }

    if (st.registrarSalidaTemporal(id)) {

        // ðŸ”¥ OBTENER SPOT DEL TICKET
        String spot = sp.obtenerSpotPorTicket(id);

        // ðŸ”¥ MARCAR SPOT COMO TEMPORAL (AMARILLO)
        sp.marcarTemporal(spot);

        JOptionPane.showMessageDialog(this,
            "Salida temporal registrada.\nEl lugar quedÃ³ marcado como TEMPORAL (amarillo).");

    } else {
        JOptionPane.showMessageDialog(this, "Error registrando la salida temporal.");
    }
}

