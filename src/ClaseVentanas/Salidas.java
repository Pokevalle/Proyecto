   public void buscarTicket() {
    String placa = Sp.getText().trim();

    if (placa.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese una placa.");
        return;
    }

    try (Connection con = dbConnection.conectar()) {

        PreparedStatement ps = con.prepareStatement(
            "SELECT ticket_id, fecha_ingreso, modo, spot_id, monto " +
            "FROM ticket WHERE placa = ? AND fecha_salida IS NULL"
        );
        ps.setString(1, placa);

        ResultSet rs = ps.executeQuery();

        DefaultTableModel modelo = (DefaultTableModel) Mostrar.getModel();
        modelo.setRowCount(0);  // limpiar tabla

        if (rs.next()) {
            String ticketId = rs.getString("ticket_id");
            Timestamp ingreso = rs.getTimestamp("fecha_ingreso");
            String modo = rs.getString("modo");
            String spot = rs.getString("spot_id");
            double montoFlat = rs.getDouble("monto");

            double montoVariable = 0;

            if (modo != null && modo.equalsIgnoreCase("VARIABLE")) {
                // Calcula horas
                long diff = System.currentTimeMillis() - ingreso.getTime();
                long horas = diff / (1000 * 60 * 60);
                if (horas == 0) horas = 1;

                montoVariable = horas * 2;  // Q2 por hora

                modelo.addRow(new Object[]{
                    ticketId, ingreso, modo, spot, "Q " + montoVariable
                });

            } else {
                // FLAT
                modelo.addRow(new Object[]{
                    ticketId, ingreso, modo, spot, "Q " + montoFlat
                });
            }

        } else {
            JOptionPane.showMessageDialog(this, "No se encontrÃ³ ticket activo.");
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}
   public void procesarSalida() {
    DefaultTableModel modelo = (DefaultTableModel) Mostrar.getModel();

    if (modelo.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Debe buscar un ticket primero.");
        return;
    }

    String ticketId = modelo.getValueAt(0, 0).toString();
    String modo = modelo.getValueAt(0, 2).toString();
    String spot = modelo.getValueAt(0, 3).toString();
    String montoTxt = modelo.getValueAt(0, 4).toString().replace("Q ", "");
    double monto = Double.parseDouble(montoTxt);

    try (java.sql.Connection con = dbConnection.conectar()) {

        // Actualizar ticket
        PreparedStatement ps1 = con.prepareStatement(
            "UPDATE ticket SET fecha_salida = NOW(), monto = ? WHERE ticket_id = ?"
        );
        ps1.setDouble(1, monto);
        ps1.setString(2, ticketId);
        ps1.executeUpdate();

        // Liberar spot
        PreparedStatement ps2 = con.prepareStatement(
            "UPDATE spots SET status = 'FREE' WHERE spot_id = ?"
        );
        ps2.setString(1, spot);
        ps2.executeUpdate();

        JOptionPane.showMessageDialog(this,
                "Salida procesada.\nTotal a pagar: Q " + monto);

        // limpiar tabla
        modelo.setRowCount(0);

    } catch (Exception e) {
        e.printStackTrace();
    }
}
