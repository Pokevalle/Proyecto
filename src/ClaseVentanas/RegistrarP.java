    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
    String tipoPersona = tipper.getSelectedItem().toString();
    double saldoFinal = 0.0; // valor por defecto

    // Solo estudiantes y catedr치ticos tienen saldo
    if (!tipoPersona.equalsIgnoreCase("VISITANTE")) {

        if (Cuen.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un saldo inicial.");
            return;
        }

        try {
            saldoFinal = Double.parseDouble(Cuen.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Saldo inv치lido. Use n칰meros (ej: 25.50).");
            return;
        }
    }

    SubPersona persona = new SubPersona(
        placa.getText().trim(),
        carnet.getText().trim(),
        nombres.getText().trim(),
        apellidos.getText().trim(),
        telefono.getText().trim(),
        saldoFinal,  // <-- ahora DOUBLE
        tipvehiculo.getSelectedItem().toString(),
        tipoPersona
    );

    persona.CrearPersona(persona);
    this.dispose();
        
    }//GEN-LAST:event_okActionPerformed

