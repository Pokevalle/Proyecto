    public void actualizarModoTiempo() {
    String modo = CM.getSelectedItem().toString();

    if (modo.equals("FLAT")) {
        Time.setVisible(false);
        LTime.setVisible(false);

        // monto fijo
        Cantidad.setText("15.00");

    } else { // VARIABLE
        Time.setVisible(true);
        LTime.setVisible(true);

        // recalcula si ya escribió algo
        String tiempoTxt = Time.getText().trim();
        if (!tiempoTxt.isEmpty()) {
            try {
                int horas = Integer.parseInt(tiempoTxt);
                double monto = horas * 2;
                Cantidad.setText(String.valueOf(monto));
            } catch (NumberFormatException ex) {
                Cantidad.setText("0.00");
            }
        } else {
            Cantidad.setText("0.00");
        }
    }
}

